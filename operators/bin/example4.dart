void main(List<String> args) {
  const age = 50;

  // Binary infix operator
  print(age + 20);
  print(age - 20);
  print(age * 20);

  print(age / 20);
  print(age ~/ 20);

  print(age % 20);

  print(age == 20);
  print(age != 20);

  print(age > 20);
  print(age < 20);
  print(age >= 20);
  print(age <= 20);

  print(age & 20); // bitwise AND
  print(age | 20); // bitwise OR
  print(age ^ 20); // bitwise XOR

  print(age << 20); // bitwise left shift
  print(age >> 20); // bitwise right shift
}
